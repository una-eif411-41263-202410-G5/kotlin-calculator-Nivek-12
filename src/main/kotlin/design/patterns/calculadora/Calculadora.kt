package design.patterns.design.patterns.calculadora

class Calculadora {

    fun suma(A: Int,B: Int): Int {
        return A+B
    }
    fun resta(A: Int,B: Int): Int {
        return A-B
    }
    fun multiplicacion(A: Int,B: Int): Int {
        return A*B
    }
    fun division(A: Int,B: Int): Int {
        return A/B
    }
}